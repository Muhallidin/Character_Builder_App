using System;
using System.Collections.Generic;
using System.Text;

namespace CharacterBuilder.Console.Commons
{
    public static class  SpecialCharacter
    {
        public static string Left_brace { get { return "{"; } }
        public static string Colon { get { return ":";  } }
        public static string Comma { get { return ","; } }
        public static string Right_brace { get { return "}"; } }
        public static string Period { get { return ". "; } }
        public static char spliter { get { return ' '; } }
        public static string Space { get { return " "; } }

    }
}
